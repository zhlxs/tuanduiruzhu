package com.cn.umessage.pojo;

public class ReturnMsg {

	public String camType;
	public String deviceMacAddr;
	public String imgBase;
	public String locationPoint;
	public String resultCode;
	public String resultMsg;
	public String version;

	public String getCamType() {
		return camType;
	}

	public void setCamType(String camType) {
		this.camType = camType;
	}

	public String getDeviceMacAddr() {
		return deviceMacAddr;
	}

	public void setDeviceMacAddr(String deviceMacAddr) {
		this.deviceMacAddr = deviceMacAddr;
	}

	public String getImgBase() {
		return imgBase;
	}

	public void setImgBase(String imgBase) {
		this.imgBase = imgBase;
	}

	public String getLocationPoint() {
		return locationPoint;
	}

	public void setLocationPoint(String locationPoint) {
		this.locationPoint = locationPoint;
	}

	public String getResultCode() {
		return resultCode;
	}

	public void setResultCode(String resultCode) {
		this.resultCode = resultCode;
	}

	public String getResultMsg() {
		return resultMsg;
	}

	public void setResultMsg(String resultMsg) {
		this.resultMsg = resultMsg;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

}
